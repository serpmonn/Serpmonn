function readJson(filePath) {
  try {
    return JSON.parse(fs.readFileSync(filePath, 'utf8'));
  } catch (err) {
    throw new Error(`${filePath}: ${err.message}`);
  }
}

function writeSortedJson(filePath, data) {
  const sorted = {};
  for (const key of Object.keys(data).sort()) {
    sorted[key] = data[key];
  }
  fs.writeFileSync(filePath, `${JSON.stringify(sorted, null, 2)}\n`, 'utf8');
}

function buildOnnmailStatic(staticKeys) {
  const onnmail = {};
  for (const key of STATIC_KEYS) {
    onnmail[key] = staticKeys[key];
  }
  return onnmail;
}

if (!fs.existsSync(localesFile)) {
  console.error(`Missing locales file: ${localesFile}`);
  process.exit(1);
}

const locales = readJson(localesFile);

const missingLocales = locales.filter((locale) => !TRANSLATIONS[locale]);
if (missingLocales.length > 0) {
  console.error(`Missing translations for locales: ${missingLocales.join(', ')}`);
  process.exit(1);
}

const extraLocales = Object.keys(TRANSLATIONS).filter((locale) => !locales.includes(locale));
if (extraLocales.length > 0) {
  console.error(`Translations exist for unknown locales: ${extraLocales.join(', ')}`);
  process.exit(1);
}

for (const locale of locales) {
  for (const key of RUNTIME_KEYS) {
    if (!TRANSLATIONS[locale].runtime[key]) {
      console.error(`[${locale}] missing runtime key: ${key}`);
      process.exit(1);
    }
  }
  for (const key of STATIC_KEYS) {
    if (!TRANSLATIONS[locale].static[key]) {
      console.error(`[${locale}] missing static key: ${key}`);
      process.exit(1);
    }
  }
}

let patchedCount = 0;

for (const locale of locales) {
  if (locale === 'en') {
    console.log('[en] Skipped i18n patch (runtime in en.base.json)');
    continue;
  }

  const filePath = path.join(i18nDir, `${locale}.json`);
  if (!fs.existsSync(filePath)) {
    console.error(`Missing i18n file: ${filePath}`);
    process.exit(1);
  }

  const data = readJson(filePath);
  for (const key of RUNTIME_KEYS) {
    data[`onnmail.${key}`] = TRANSLATIONS[locale].runtime[key];
  }
  writeSortedJson(filePath, data);
  patchedCount++;
  console.log(`[${locale}] Patched runtime keys`);
}

const onnmailData = {};
for (const locale of locales) {
  onnmailData[locale] = {
    onnmail: buildOnnmailStatic(TRANSLATIONS[locale].static),
  };
}

fs.mkdirSync(path.dirname(onnmailDataFile), { recursive: true });
fs.writeFileSync(onnmailDataFile, `${JSON.stringify(onnmailData, null, 2)}\n`, 'utf8');

console.log('\nSummary:');
console.log(`- Locales patched: ${patchedCount}`);
console.log(`- onnmail.json created with ${locales.length} locales`);
console.log(`- Locales covered: ${locales.join(', ')}`);

function englishDefaults() {
  const out = {};
  for (const key of avatarKeys) {
    out[key] = base[key];
  }
  out['profile.avatarFileNotSelected'] = 'No file selected';
  out['profile.avatarUserNotFound'] = 'User not found';
  return out;
}

const enDefaults = englishDefaults();
const locales = JSON.parse(fs.readFileSync(localesFile, 'utf8'));
let updated = 0;

for (const locale of locales) {
  const filePath = path.join(i18nDir, `${locale}.json`);
  const data = JSON.parse(fs.readFileSync(filePath, 'utf8'));
  const pack = { ...enDefaults, ...(OVERRIDES[locale] || {}) };
  let changed = false;

  for (const [key, value] of Object.entries(pack)) {
    if (data[key] !== value) {
      data[key] = value;
      changed = true;
    }
  }

  if (changed) {
    const sorted = Object.fromEntries(Object.keys(data).sort().map((k) => [k, data[k]]));
    fs.writeFileSync(filePath, `${JSON.stringify(sorted, null, 2)}\n`);
    updated += 1;
    console.log(`updated ${locale}`);
  }
}

console.log(`Done. Updated ${updated} locale files.`);

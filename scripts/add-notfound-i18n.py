def upsert(path: Path, keys: dict) -> None:
    data = json.loads(path.read_text(encoding="utf-8"))
    data.update(keys)
    path.write_text(json.dumps(data, ensure_ascii=False, indent=2) + "\n", encoding="utf-8")


def main() -> None:
    base_path = I18N / "en.base.json"
    upsert(base_path, EN)
    print("updated", base_path.name)

    # en.json may override base; keep notFound in base, also mirror into en.json for completeness
    en_path = I18N / "en.json"
    if en_path.exists():
        upsert(en_path, EN)
        print("updated", en_path.name)

    for path in sorted(I18N.glob("*.json")):
        name = path.name
        if name in {"en.base.json", "en.json"}:
            continue
        code = name[:-5]
        keys = TRANSLATIONS.get(code)
        if not keys:
            # fallback to English for any missing locale file
            keys = EN
            print("fallback EN ->", name)
        upsert(path, keys)
        print("updated", name)


if __name__ == "__main__":
    main()
